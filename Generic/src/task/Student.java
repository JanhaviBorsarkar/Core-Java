package task;
public class Student 
{
	// defining variables
	private int rollno;
	private String name;
	
	//Generating getter and setter
	public void setRollno(int rollno) 
	{
		this.rollno = rollno;
	}
	public int getRollno() 
	{
		return rollno;
	}
	
	public void setName(String name) 
	{
		this.name = name;
	}
	public String getName() 
	{
		return name;
	}
}
